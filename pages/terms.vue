        The Employee Retention Credit - as released March 12, 2020 and amended
        Dec 27, 2020 - in place to provide incentive to employers who wish to
        keep their employees on payroll despite difficulty due to COVID-19. A
        brief description is available here, not as a matter of advice, but just
        to promote understanding of the law.
