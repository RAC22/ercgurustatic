      <h5>Additional Items to Note:</h5>
      <p>
        Generally payroll is a cost that your business is already incurring.
        Here is a factor of what taxes are remitted for each $10,000 qualifying
        amount. If taxes were not remitted prior to our filing, it is not
        usually expected that additional cash costs will be incurred, but rather
        the IRS will deduct the taxes that would have been owed from the total
        credit.
      </p>
      <p>
        2020 - FICA/Medicare - 15.3% or $1,530 is remitted as federal payroll
        tax deposits. Income Tax - Withholding is elective and totally dependent
        on the employee's situation. The $10,000 of wages claimed for the credit
        is $10,000 in taxable income to the employee in 2020 - as is all payroll
        - to be filed with their regular income taxes before April 15, 2021.
      </p>
      <p>
        2021 - FICA/Medicare - 9.1% or $910 must be remitted - or rather, 6.2%
        can be skipped as part of normal tax deposits for a qualifying business
        in a given quarter. Income Tax - Withholding is elective and totally
        dependent on the employee’s situation. The $10,000 of wages claimed for
        the credit is $10,000 in taxable income to the employee in 2021 - as is
        all payroll - to be filed with their regular income taxes before April
        15, 2022.
      </p>
      <p>
        Unemployment Insurance- Localities have a requirement to remit
        unemployment insurance on each employee. Rates for this insurance vary
        by industry and location - but generally range from 0.5 - 3% of total
        payroll.
      </p>
      <p>
        Penalties - Filing late results in a 5% monthly penalty on the tax due
        up to 25%. Late deposits result in up to 15% penalty. Overall you can
        pay up to a 40% penalty on your 15.3% payroll tax.
      </p>
