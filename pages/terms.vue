      <p>Businesses qualify for this credit based on one of:</p>
      <br />
      <p>Loss of Revenue</p>
      <br />
      <p>
        2020: 50% quarter over quarter reduction in gross sales for 2020 vs 2019
      </p>
      <p>
        2021: 20% quarter over quarter reduction in gross sales for 2021 vs 2019
        or 2020 vs 2021
      </p>
      <br />
      <p>OR</p>
      <br />
      <p>Full or partial shutdown</p>
      <p>
        Of the client business due to legitimate government orders that restrict
        business in a substantial way.
      </p>
      <br />
      <p>Available Credit:</p>
      <p>
        2020: 50% credit on up to $10,000 of wages per employee for the entire
        year. This means a maximum credit of $5,000 per employee in 2020.
      </p>
      <p>
        2021: 70% credit on up to $10,000 of wages per employee per quarter
        through Dec 31. This means a maximum credit of $7,000 per quarter due.
      </p>
